export { DEFAULT_SCHOOL_COLOR, SCHOOL_COLORS };

export function getSchoolColor(school: string): SchoolColorPair {
  const key = school.toLowerCase();
  return (
    SCHOOL_COLORS[key] ??
    SCHOOL_COLORS[SCHOOL_ALIASES[key] ?? ''] ??
    DEFAULT_SCHOOL_COLOR
  );
}

export function schoolColorStyle(school: string): React.CSSProperties {
  const c = getSchoolColor(school);
  return {
    '--school-primary': c.primary,
    '--school-secondary': c.secondary,
  } as React.CSSProperties;
}
